<script lang="ts">
import I18nKey from "@i18n/i18nKey";
import { i18n } from "@i18n/translation";
import Icon from "@iconify/svelte";
import { clearHue, getDefaultHue, getHue, setHue } from "@utils/setting-utils";

let hue = getHue();
const defaultHue = getDefaultHue();
// 检查色调是否在本地存储中持久化，以避免在初始化时覆盖随机的色调
const isPersisted = !!localStorage.getItem("hue");
let isInitialized = false;

function resetHue() {
	clearHue();
	hue = getDefaultHue();
	// 重置初始化标志以防止监视程序保存重置值
	isInitialized = false;
}

$: if (hue || hue === 0) {
	if (isInitialized) {
		setHue(hue);
	} else if (isPersisted) {
		// 如果持久化，我们可以设置它（尽管是冗余的，是安全的）
		setHue(hue);
		isInitialized = true;
	} else {
		// 如果未持久化（随机色调），我们不保存它到本地存储
		// 但是我们需要将初始化标志设置为 true，以便后续更改被保存
		// 然而，我们必须小心不要立即设置它，否则初始值将被保存
		// 我们可以假设如果值从初始值改变，它是一个用户操作
		if (hue !== defaultHue) {
			setHue(hue);
			isInitialized = true;
		}
	}
}
</script>

<div id="display-setting" class="float-panel float-panel-closed absolute transition-all w-80 right-4 px-4 py-4">
    <div class="flex flex-row gap-2 mb-3 items-center justify-between">
        <div class="flex gap-2 font-bold text-lg text-neutral-900 dark:text-neutral-100 transition relative ml-3
            before:w-1 before:h-4 before:rounded-md before:bg-[var(--primary)]
            before:absolute before:-left-3 before:top-[0.33rem]"
        >
            {i18n(I18nKey.themeColor)}
            <button aria-label="Reset to Default" class="btn-regular w-7 h-7 rounded-md  active:scale-90 will-change-transform"
                    class:opacity-0={hue === defaultHue} class:pointer-events-none={hue === defaultHue} on:click={resetHue}>
                <div class="text-[var(--btn-content)]">
                    <Icon icon="fa6-solid:arrow-rotate-left" class="text-[0.875rem]"></Icon>
                </div>
            </button>
        </div>
        <div class="flex gap-1">
            <div id="hueValue" class="transition bg-[var(--btn-regular-bg)] w-10 h-7 rounded-md flex justify-center
            font-bold text-sm items-center text-[var(--btn-content)]">
                {hue}
            </div>
        </div>
    </div>
    <div class="w-full h-6 px-1 bg-[oklch(0.80_0.10_0)] dark:bg-[oklch(0.70_0.10_0)] rounded select-none">
        <input aria-label={i18n(I18nKey.themeColor)} type="range" min="0" max="360" bind:value={hue}
               class="slider" id="colorSlider" step="5" style="width: 100%">
    </div>
</div>


